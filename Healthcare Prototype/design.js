function openPopup() {
    // Get the popup box element
    var popup = document.getElementById("popupBox");
    
    // Show the popup box
    popup.style.display = "block";
  }
  
  function closePopup() {
    // Get the popup box element
    var popup = document.getElementById("popupBox");
    
    // Hide the popup box
    popup.style.display = "none";
  }